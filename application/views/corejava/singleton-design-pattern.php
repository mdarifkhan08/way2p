Singleton Design Pattern
</div>
<div class="panel panel-body">
Singleton design pattern restrict a class to create multiple object, it allow us to share same copy of the object.
</div>
</div>



<div class="panel panel-primary">
<div class="panel panel-heading">
Why we need singleton design pattern?
</div>
<div class="panel panel-body">
when we create multiple object of same class then it will take memory inside server and somehow we are wasting memory of server and it cause degrading performance.<br/><br/>

so that we need to have one object and use it multiple times and singleton design pattern help us to do that.
</div>
</div>



