How to create singleton design pattern?
</div>
<div class="panel panel-body">
1. It must have private constructor.<br/>
(when we have private constructor then we can not create object of the class from any other class.)<br/><br/>


2. It must have private static variable with data type of variable is same class.<br/><br/>


3.It must have public static factory method having the logic of singleton<br/><br/>

Factory Method:- the method of class capable of creating and returning same class or other class object is known as factory method.

